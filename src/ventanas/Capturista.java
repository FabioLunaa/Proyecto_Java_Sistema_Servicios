    private void jButton_ImprimirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ImprimirClienteActionPerformed

        Document documento = new Document();//si o si creamos un ob de la clase Document.
        try {

            String ruta = System.getProperty("user.home");//estas 2 lineas son del lugar de guardado del pdf.
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/" + " Listado de clientes "+ " .pdf"));
    //3 lineas d obtencion de la imagen.
            com.itextpdf.text.Image header = com.itextpdf.text.Image.getInstance("src/images/BannerPDF.jpg");
            header.scaleToFit(650, 1000);
            header.setAlignment(Chunk.ALIGN_CENTER);
    //4 lineas parrafo y sus modif.
            Paragraph parrafo = new Paragraph();
            parrafo.setAlignment(Paragraph.ALIGN_CENTER);
            parrafo.add("Listado de clientes \n \n");
            parrafo.setFont(FontFactory.getFont("Tahoma" + 18 + Font.BOLD + BaseColor.DARK_GRAY));
    //guardado de doc.. header y parrafo creado anteriormente.
            documento.open();
            documento.add(header);
            documento.add(parrafo);
    // creo ob de la clase PdfTable y agrego las celdas(columnas) con sus nombres.
            PdfPTable tabla = new PdfPTable(5);
            tabla.addCell("ID");
            tabla.addCell("Nombre");
            tabla.addCell("Email");
            tabla.addCell("Telefono");
            tabla.addCell("Direccion");
            
            try {
                Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select * from cliente ");
            ResultSet rs = pst.executeQuery();
            
                if (rs.next()) {
                    do {                        
                        tabla.addCell(rs.getString(1));//agrego celdas con la ubicacion ordenada de lo q viene de la BD.
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        tabla.addCell(rs.getString(5));
                        
                    } while (rs.next());
                    documento.add(tabla);//agrego la tabla al doc.
                }
            } catch (SQLException e) {
                System.err.println("Error al generar lista de clientes." + e);
            }
            documento.close();
            JOptionPane.showMessageDialog(null, "Â¡Lista de cliente guardado correctamente!");
        } catch (Exception e) {
            System.err.println("ERROR al cargar PDF." + e);   
        }
    }//GEN-LAST:event_jButton_ImprimirClienteActionPerformed

