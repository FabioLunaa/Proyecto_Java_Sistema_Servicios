    private void jButton_actualizarEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarEquipoActionPerformed
    //creamos estas var para guardar lo q este en los cmb, txt, jlabel.
       String estatus, ComentariosTecnicos, tecnico;
       
       estatus = cmb_estatus.getSelectedItem().toString();
       ComentariosTecnicos = jTextPane_comentariosTecnicos.getText();
       tecnico = jLabel_revisionTecnicaDe.getText();
       
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
     "update equipos set estatus=?, comentarios_técnicos=?, revision_tecnica_de=? where id_equipo = '" + idEquipo + "'");
            //guardamos en la bd los datos q colocamos en las var creadas.
            pst.setString(1, estatus);
            pst.setString(2, ComentariosTecnicos);
            pst.setString(3, user);
            
            pst.executeUpdate();
            cn.close();
            JOptionPane.showMessageDialog(null, "¡Actualización exitosa!");
            this.dispose();
            
        } catch (SQLException e) {
            System.err.println("ERROR al actualizar equipo." + e);
            JOptionPane.showMessageDialog(null, "Error al actualizar equipo. ¡Contacte al administrador.!");
        }
    }//GEN-LAST:event_jButton_actualizarEquipoActionPerformed

