    public Informacion_Equipo_Tecnico() {
        initComponents();
        user = Login.user;
        idEquipo = Gestionar_equipos.IDequipo_update;
        
        //acá cargamos cada cmb,txt o jtext con la info de la bd.
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select * from equipos where id_equipo = '" + idEquipo + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                //guardo en casa cmb, txt y jtextPane lo q hay en la bd.
                cmb_tipoEquipo.setSelectedItem(rs.getString("tipo_equipo"));
                cmb_marca.setSelectedItem(rs.getString("marca"));
                cmb_estatus.setSelectedItem(rs.getString("estatus"));
                txt_modelo.setText(rs.getString("modelo"));
                txt_numSerie.setText(rs.getString("num_serie"));
                txt_ultimaModificacion.setText(rs.getString("ultima_modificación"));

                String dia= "", mes = "", annio = "";//creo new var de fechas para traer las fechas de la bd.
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("anio_ingreso");

                txt_fechaIngreso.setText(dia + " del " + mes + " del " + annio);//guardo las fechas.

                jTextPane_dañosObservaciones.setText(rs.getString("observaciones"));
                jTextPane_comentariosTecnicos.setText(rs.getString("comentarios_técnicos"));
                jLabel_revisionTecnicaDe.setText("Comentarios y actualización del técnico: " + rs.getString("revision_tecnica_de"));

            } 
        } catch (SQLException e) {
            System.err.println("ERROR al consultar la información del equipo " + e);
        }
        setTitle("Equipos registrado con el ID " + idEquipo + " - Sesión de " + user );
        setSize(600, 520);
        setResizable(false);
        setLocationRelativeTo(null);

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_wallpaper.getWidth(),
                jLabel_wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_wallpaper.setIcon(icono);
        this.repaint();

    }

