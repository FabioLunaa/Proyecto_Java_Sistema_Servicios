    private void jButton_RegistrarClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegistrarClientActionPerformed
        
        int validacion = 0;//var. bandera.
        String nombre, email, telefono, direccion;
        
        nombre = txt_nombre.getText().trim();
        email = txt_email.getText().trim();
        telefono = txt_telefono.getText().trim();
        direccion = txt_direccion.getText().trim();
        
        if (nombre.equals("")) {
            txt_nombre.setBackground(Color.red);
            validacion++;
        }
        if (email.equals("")) {
            txt_email.setBackground(Color.red);
            validacion++;
        }
        if (telefono.equals("")) {
            txt_telefono.setBackground(Color.red);
            validacion++;//hace q la validacion tenga un valor !0
        }
        
        if (direccion.equals("")) {
            txt_direccion.setBackground(Color.red);
            validacion++;
        }
        
        if (validacion == 0) {
            
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                   //instrucciones para guardar datos dentro de la bd.     
                        "insert into cliente values (?,?,?,?,?,?)");
                
                pst.setInt(1, 0);
                pst.setString(2, nombre);
                pst.setString(3, email);
                pst.setString(4, telefono);
                pst.setString(5, direccion);
                pst.setString(6, user);
               
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();//a este metodo lo programamos al final de todo el c칩digo.
                
                txt_nombre.setBackground(Color.green);
                txt_email.setBackground(Color.green);
                txt_telefono.setBackground(Color.green);
                txt_direccion.setBackground(Color.green);
                
                
                 JOptionPane.showMessageDialog(null, "Registro exitoso.");
                this.dispose();//metodo q libera recurso y cierra la interfaz
                
            } catch (SQLException e) {
                System.out.println("Error al registrar cliente." + e);
                JOptionPane.showMessageDialog(null, "Error al registrar cliente. Contacte al Administrador.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes completar todos los campos.");
        }
    }//GEN-LAST:event_jButton_RegistrarClientActionPerformed

